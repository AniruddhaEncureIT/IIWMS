// ── helpers ──────────────────────────────────────────────────────────────────
const DOC = (
  id: string,
  name: string,
  type: string,
  uploadedAt: string,
  uploadedBy = "Rajesh Kumar"
): IDocument => ({ id, name, type, uploadedAt, uploadedBy });

const DS = (
  drawings: IDocument[],
  sitePhotos: IDocument[],
  surveyReports: IDocument[]
): IProjectDocumentSets => ({ drawings, sitePhotos, surveyReports });
let hSeq = 0;
const H = (
  action: string,
  performedBy: string,
  performedAt: string,
  fromStatus: string,
  toStatus: string,
  remarks?: string
): IProjectHistory => ({
  id: `H${++hSeq}`,
  action,
  performedBy,
  performedAt,
  fromStatus,
  toStatus,
  remarks,
});

const SW = (id: string, name: string, category?: string, description?: string): ISubWork => ({
  id,
  name,
  category,
  description,
});

const LS = (
  id: string,
  materialName: string,
  sourceOfSupply: string,
  kilometer: number
): ILeadStatement => ({ id, materialName, sourceOfSupply, kilometer, calculation: kilometer * 150 });

const RA = (
  id: string,
  itemNumber: string,
  itemDescription: string,
  material: number,
  labor: number,
  machinery: number,
  baseRate: number,
  remarks?: string
): IRateAnalysis => ({
  id,
  itemNumber,
  itemDescription,
  materialComponents: material,
  laborComponents: labor,
  machineryComponents: machinery,
  baseRate,
  totalRate: material + labor + machinery + baseRate,
  remarks,
});

const MS = (
  id: string,
  itemNumber: string,
  itemName: string,
  length: number,
  breadth: number,
  height: number,
  unit: string,
  rate: number
): IMeasurement => {
  const quantity = length * breadth * height;
  return { id, itemNumber, itemName, length, breadth, height, quantity, unit, rate, amount: quantity * rate };
};

